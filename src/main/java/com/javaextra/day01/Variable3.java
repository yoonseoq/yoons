package com.javaextra.day01;

public class Variable3 {
    public static void main(String[] args) {
        int n=10;
        System.out.println(n++);//보통 이걸 많이씀
        System.out.println(n++);
        System.out.println(n);
        System.out.println("-----------------");
        int n2=10;
        System.out.println(++n2);
        System.out.println(++n2);
        System.out.println(n2);
    }
}
